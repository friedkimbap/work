public abstract class AndroidStudio {
    public void startAS(){
        System.out.println("안드로이드 스튜디오를 실행합니다.");
    }
    public void codingASApp(){
        System.out.println("안드로이드 앱을 코딩합니다.");
    }
    public void compileASApp(){
        System.out.println("안드로이드 앱을 컴파일합니다.");
    }
    public abstract void runASApp();
}
